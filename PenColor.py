import turtle

# Creating turtle turtle
t = turtle.Turtle()

# Increase the turtle size
t.shapesize(3, 3, 3)
turtle.bgcolor("black")
# fill the color
t.fillcolor("blue")

# Change the pen color
t.pencolor("yellow")
# t.color("green", "red")  #It can change the background and pen color as well as.

# t.pencolor("red")
t.fd(122)

turtle.mainloop()